package com.btsinfo.evaluation_des;

public class Des {

    int idDes;
    String imgDes;

    public Des(int idDes, String imgDes) {
        this.idDes = idDes;
        this.imgDes = imgDes;
    }

    public int getIdDes() {
        return idDes;
    }

    public void setIdDes(int idDes) {
        this.idDes = idDes;
    }

    public String getImgDes() {
        return imgDes;
    }

    public void setImgDes(String imgDes) {
        this.imgDes = imgDes;
    }
}


